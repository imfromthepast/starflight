function drawShip(options){
	var shipCont = new Container(),
		shipShape = new Shape(),
		engineFlare = new Shape(),
		missles = new Shape(),
		laser = new Shape(),
		details = new Shape(),
		baseColor = options.color!=null?options.color:'#555',
		greyColor = options.color!=null?options.color:'grey',
		lightColor = options.color!=null?options.color:'#888',
		darkColor = options.color!=null?options.color:'#333',
		hullY = 0			

	if(ship.missileClass>0 && options.drawWeapons){
		missles.graphics.f(greyColor).dc(23,95,5).dc(77,95,5).ef()
		if(ship.weaponsArmed)missles.graphics.f('red').dr(18,85+hullY,3,8).dr(79,85,3,8).ef()
	}
	shipShape.graphics
		.f(baseColor)
		//.s(darkColor)
		// left engine
		// 
		// .rr(20,90+hullY,15,40,5)
		// .beginFill(darkColor)
		// .rr(23,93+hullY,9,34,3)
		// 
		// right engine
		// .beginFill(baseColor)
		// .rr(65,90+hullY,15,40,5)
		// .beginFill(darkColor)
		// .rr(68,93+hullY,9,34,3)
		// hull
		// .beginFill(baseColor)
		// .dr(30,80+hullY,40,20)
		// neck
		// .beginFill(darkColor)
		// .dr(45,30,10,50+hullY)
		// wings
		//.beginFill(baseColor)
		//.es()
		
		.f(baseColor)
		.mt(50,30)
		.lt(25,30)
		.lt(25,27)
		.lt(40,15)
		.lt(60,15)
		.lt(75,27)
		.lt(75,30)
		.s(darkColor)
		.ss(0.25)
		.dr(28,27,15,4)
		.dr(58,27,15,4)
		// bridge
		.s(darkColor).ss(0.5)
		.beginRadialGradientFill([lightColor,baseColor], [0, 1], 50, 20, 0, 50, 20, 15)
		.dc(50,20,15).ef()
		.f(darkColor)
		.mt(40,20)
		.qt(40,10,50,10)
		.qt(60,10,60,20)
		.lt(55,20)
		.qt(55,15,50,15)
		.qt(45,15,45,20)
		.lt(40,20)
		.ef()
		.es()
	details.graphics
		.s(darkColor).ss(0.25)
		.dc(50,20,7)
		.dc(50,20,13)
		.dc(50,20,3)
		.es()
		.s('black').ss(0.25)
		.f(darkColor)
		.dr(28,94,4,24)
		.dr(68,94,4,24)
		.f(greyColor)
		.dr(25,96,5,8)
		.dr(71,96,5,8)
		.dr(25,106,5,8)
		.dr(71,106,5,8)
		.dr(25,116,5,8)
		.dr(71,116,5,8)

	var bridge = drawInterstelBridge(darkColor,lightColor,baseColor)
		bridge.scaleX=0.18
		bridge.scaleY = 0.18
		bridge.x=50
		bridge.y=34
	var engineScale = 0.1
	var leftEngine = drawInterstelEngine(lightColor,baseColor,darkColor)
	var rightEngine = flipX(leftEngine)
		leftEngine.scaleX=engineScale
		leftEngine.scaleY=engineScale
		leftEngine.y=85
		leftEngine.x=33
		rightEngine.scaleX=-engineScale
		rightEngine.scaleY=engineScale
		rightEngine.y=85
		rightEngine.x=68
	if(ship.engineClass==0){
		leftEngine.visible=false
		rightEngine.visible=false
	}
	var hull = buildInterstelHull(lightColor,baseColor,darkColor)
		hull.scaleX=0.15
		hull.scaleY=0.15
		hull.x=50
		hull.y=90
	var neck = buildInterstelNeck(lightColor,darkColor,baseColor)
		neck.scaleX=0.15
		neck.scaleY=0.2
		neck.x=50
		neck.y=82
	var shieldGenerator = new Shape()
	shieldGenerator.graphics.f(greyColor).s('black').ss(0.25).dr(45,77,10,5)
	shieldGenerator.visible=false
	if(ship.laserClass>0 && options.drawWeapons){
		laser.graphics.f(greyColor).s('black').ss(0.5).dc(50,92,5).ef()
		if(ship.weaponsArmed)laser.graphics.f('red').dr(45,83+hullY,3,8).dr(52,83,3,8).ef()
	}
	if(ship.plasmaClass>0 && options.drawWeapons){
		shipShape.graphics.f(greyColor).dc(50,25,5).ef()
		if(ship.weaponsArmed)shipShape.graphics.f('red').dr(48,15,4,10).ef()
	}
	engineFlare.name='engineFlare'
	//if(ship.enginesOn)shipShape.graphics
	engineFlare.graphics.beginLinearGradientFill(['white','purple','rgba(0,0,0,0)'], [0,0.25,1], 0, 130, 0, 160)
		.de(20,130,15,40)
		.de(65,130,15,40)
		.ef()
	if(ship.shieldClass>0){
		shieldGenerator.visible = true 
	}
	if(ship.shieldsUp && options.drawShields && ship.shieldClass>0){
		var shieldCoords = [50,-10,90,-10,90,80,90,160,50,160,10,160,10,80,10,-10,50,-10]
		shipShape.graphics
			.beginStroke('red')
			.ss(!options.animateShields?5:ship.shieldsVal<20?1:Math.round(ship.shieldsVal/20))
			.mt(50,-10)
			.qt(90,-10,90,80)
			.qt(90,160,50,160)
			.qt(10,160,10,80)
			.qt(10,-10,50,-10)
			.es()
	}

	var x,y
	if(options.drawPods){
		for (var i = 0; i < ship.cargoPods; i++) {
			if(isEven(i)){
				x = 40
				y = i*3+35
			}else{
				x = 55
			}	
			shipShape.graphics.beginFill(greyColor).dr(x,y,5,5)
			if(!isEven(i))
				shipShape.graphics.beginFill(greyColor).dr(x-2,y+2,2,1)
			else			
				shipShape.graphics.beginFill(greyColor).dr(x+5,y+2,2,1)			
		}
		for (var i = 0; i < ship.blastoPods; i++) {
			if(isEven(i)){
				x = 40
				y = i*3+65
			}else{
				x = 55
			}	
			shipShape.graphics.beginFill(lightColor).dr(x,y,5,5)						
		}
		for (var i = 0; i < ship.jumpPods; i++) {
			if(isEven(i)){
				x = 40
				y = i*3+75
			}else{
				x = 55
			}	
			shipShape.graphics.beginFill(darkColor).dr(x,y,5,5)						
		}
	}
	engineFlare.visible = ship.enginesOn

	shipCont.addChild(bridge,shipShape,missles,leftEngine,rightEngine,hull,neck,shieldGenerator,laser,details,engineFlare)
	
	for (var i = 0; i < 8; i++) {
		var line = new Shape()
		line.graphics
			.s(darkColor).ss(0.25)
			.mt(0,0)
			.lt(12,0)
			.es()
		line.regX = 15
		line.regY = 0
		line.x=50
		line.y=20
		line.rotation = 45*i
		shipCont.addChild(line)
	}	


	shipCont.scaleX = options.scale
	shipCont.scaleY = options.scale
	return shipCont
}
